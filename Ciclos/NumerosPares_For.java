public class NumerosPares_For {
    public static void main(String[] args) {
        System.out.println("*** NUMEROS PARES DEL 0 AL 20 (FOR) ***");
        //   Inicial  ; Hasta   ; Incremento de mas un digito
        for(int i = 0 ; i <= 20 ; i++)
            {
            if (i % 2 == 0) 
                System.out.print(i + " ");              
            }
    }
    
}
