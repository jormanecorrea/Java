public class IntroduccionArreglos {
    public static void main(String[] args) {
        // ARREGLOS
        //1. Declarar un Arreglo(ARRAY)
        int[] enteros; //Se escribe en plural ya que son varios datos
        //2. Inicializar el arreglo
        enteros = new int[3]; //Se crea objeto de tipo Arreglo

        // Otra forma de Declarar e inicializar un arreglo
        int[] numeros = new int[2];
        //O usando Var
        var numeros2 = new int[2];
    }
}
